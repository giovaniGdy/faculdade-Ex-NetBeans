    private void excluirButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirButtonActionPerformed
        // TODO add your handling code here:
        int resp = JOptionPane.showConfirmDialog(this, 
                "Deseja excluir registro", "Excluir", JOptionPane.YES_NO_OPTION);
        
        if (resp == JOptionPane.YES_OPTION) {
            int linha = contatosTable.getSelectedRow();
            int id = (Integer)contatosTable.getModel().getValueAt(linha, 0);
            
            try {
                
                Excluir excluir = new Excluir();
                excluir.excluir(id);
                
                procurarButton.doClick();
            } catch(Exception e) {
                JOptionPane.showMessageDialog(this, 
                        "Opps! Ocorreu um erro: "+ e.getMessage());
            }
        }
    }//GEN-LAST:event_excluirButtonActionPerformed

