    private void procurarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_procurarButtonActionPerformed
        // TODO add your handling code here:
        
        try {
            setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
            
            Consultar consultar = new Consultar();
            List<Contato> lista = consultar.consultar(procurarField.getText());
            
            DefaultTableModel model = (DefaultTableModel) contatosTable.getModel();
            model.setRowCount(0);
            
            for (Contato contato: lista) {
                model.addRow(new Object[] {
                    contato.getId(),
                    contato.getNome(),
                    contato.getEmail(),
                    contato.getDataNasc()
                });
            }
            
            setCursor(Cursor.getDefaultCursor());
        } catch(Exception e) {
            JOptionPane.showMessageDialog(this, 
                    "Opps! Ocorreu um erro: " + e.getMessage());
        }
    }//GEN-LAST:event_procurarButtonActionPerformed

